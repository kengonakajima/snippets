#include <stdio.h>

#include <map>

class X
{
    int x[10];
    X(int a){
        for(int i=0;i<100;i++){
            x[i] = a;
        }
    }
};

int main(int argc, char **argv)
{
    std::map<int,int> mx;
    int N = atoi(argv[1]);
    int D = atoi(argv[2]);

    for(int i=0;i<N;i++){
        mx[rand()%(N/D)] = i;
    }
    printf("%d\n", mx[N/D/10] );
    printf("%d\n", mx[N/D/5] );
    return 0;
}
